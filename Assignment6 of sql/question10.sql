SELECT title, number_of_pages
FROM books
WHERE title LIKE 'A%' OR
      title LIKE 'B%' OR
      title LIKE 'C%' OR
      title LIKE 'D%' OR
      title LIKE 'E%' OR
      title LIKE 'F%' OR
      title LIKE 'G%' OR
      title LIKE 'H%' OR
      title LIKE 'I%' OR
      title LIKE 'J%' OR
      title LIKE 'K%';